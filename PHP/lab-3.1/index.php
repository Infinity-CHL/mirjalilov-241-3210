// 25-вопрос: 
// Удаление файла.Пусть в корне вашего сайта лежат файлы 1.txt, 2.txt и 3.txt. Вручную сделайте массив с именами этих файлов. Переберите его циклом и удалите все эти файлы.

// Массив с именами файлов
$files = ['1.txt', '2.txt', '3.txt'];

// Перебор массива и удаление файлов
foreach ($files as $file) {
    if (file_exists($file)) {
        if (unlink($file)) {
            echo "$file — удалён<br>";
        } else {
            echo "$file — не удалось удалить<br>";
        }
    } else {
        echo "$file — не существует<br>";
    }
}


