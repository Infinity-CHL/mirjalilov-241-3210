<?php
// Задание: Операторы присваивания. Дано: $a = 148; $b = 76; $c = ' голубя'; Найти: Используя операторы присваивания (с конкатенацией) и заданные переменные, получить предложение "72 голубя"
// Исходные данные
$a = 148;
$b = 76;
$c = ' голубя';

// Вычисляем 72
$result = $a - $b; // 148 - 76 = 72

// Конкатенация с переменной $c
$result .= $c; // "72" + " голубя" = "72 голубя"

// Выводим результат
echo $result; // "72 голубя"
?>