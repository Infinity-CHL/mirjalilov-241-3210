<?php
// Задание: Условный оператор. Дано: $a = 0;$b = 12; Найти: Если значение $a истинно, то найти отношение $b к $a и вывести на экран, иначе привести $a к булевому типу данныхи вывести на экран.
// Исходные данные
$a = 0;
$b = 12;

// Условный оператор
if ($a) {
    // Если $a истинно, вычисляем отношение $b / $a
    $result = $b / $a;
    echo "Отношение \$b / \$a = $result";
} else {
    // Если $a ложно, приводим $a к булевому типу
    $boolA = (bool)$a;
    echo "Приведенное значение \$a: " . ($boolA ? 'true' : 'false');
}
?>