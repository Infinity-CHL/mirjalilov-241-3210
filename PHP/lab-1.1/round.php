<?php
// Задание: Округление. Дано: $a = 5.7; $b = 4.2; $c = '7.4'; $d = '8.9кг'; Найти: Значение пола и потолка переменных. Выполнить арифмитеческое округление переменных.
// Исходные данные
$a = 5.7;
$b = 4.2;
$c = '7.4';
$d = '8.9кг';

// Преобразуем строки в числа
$c = (float)$c;
$d = (float)$d; // Преобразует "8.9кг" в 8.9


// Функция для вывода результатов
function printRoundResults($value) {
    echo "Исходное значение: $value\n"; 
    echo "floor: " . floor($value) . "\n"; // floor() — округление вниз (до ближайшего меньшего целого).
    echo "ceil: " . ceil($value) . "\n"; // ceil() — округление вверх (до ближайшего большего целого).
    echo "round: " . round($value) . "\n"; // round() — арифметическое округление (до ближайшего целого).
    echo "-----------------\n";
}

// Выводим результаты для каждой переменной
printRoundResults($a);
printRoundResults($b);
printRoundResults($c);
printRoundResults($d);
?>